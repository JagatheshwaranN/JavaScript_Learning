// Data Types

// String
console.log(typeof("JavaScript"));

// Number
console.log(typeof(10));

// Boolean
console.log(typeof(true));

let myVariable;
// Undefined
console.log(typeof(myVariable));

// Initial Value
myVariable = "Welcome";
console.log(myVariable);

// Updated Value
myVariable = 10;
console.log(myVariable);

let myNumber = 15;

// Addition
let myCombo = myNumber + myVariable;
console.log(myCombo);

// Concatenation
myCombo = myNumber + "Welcome";
console.log(myCombo);

// Boolean Operations
myvalue = 20;
console.log(myvalue === 20);
console.log(myvalue === 10);
console.log(myvalue !== 10);

let myBooleanVal = myvalue === 20;
console.log(myBooleanVal);

console.log("Hello World");